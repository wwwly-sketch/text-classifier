package com.classifier.model;

/**
 * Перечисление возможных тематик текста.
 * Каждая тематика имеет отображаемое имя на русском языке.
 *
 * @version 1.0.0
 */
public enum Topic {

    /** Медицинская тематика. */
    MEDICINE("Медицина"),

    /** Историческая тематика. */
    HISTORY("История"),

    /** Тематика программирования. */
    PROGRAMMING("Программирование"),

    /** Тематика компьютерных сетей. */
    NETWORKS("Сети"),

    /** Криптографическая тематика. */
    CRYPTOGRAPHY("Криптография"),

    /** Финансовая тематика. */
    FINANCE("Финансы");

    /** Отображаемое имя тематики на русском языке. */
    private final String displayName;

    /**
     * Создает тематику с указанным отображаемым именем.
     *
     * @param displayName отображаемое имя тематики
     */
    Topic(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Возвращает отображаемое имя тематики на русском языке.
     *
     * @return отображаемое имя тематики
     */
    public String getDisplayName() {
        return displayName;
    }
}
